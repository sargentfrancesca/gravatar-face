class AvatarHash():
	def __init__ (self, username, filename, gender, first_eye_x, first_eye_y, frontalface_x, frontalface_y, frontalface_width, frontalface_height, second_eye_x, second_eye_y, hashjuice, avatar_hash, avatar_url):
		self.username = username
		self.filename = filename
		self.gender = gender
		self.first_eye_x = first_eye_x
		self.first_eye_y = first_eye_y
		self.frontalface_x = frontalface_x
		self.frontalface_y = frontalface_y
		self.frontalface_width = frontalface_width
		self.frontalface_height = frontalface_height
		self.second_eye_x = second_eye_x
		self.second_eye_y = second_eye_y
		self.hashjuice = hashjuice
		self.avatar_hash = avatar_hash
		self.avatar_url = avatar_url